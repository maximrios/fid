<div class="col-lg-12">
	<h4 class="titulo_seccion">Objetivo Principal</h4>
	<hr>
	<p>Nuestro OBJETIVO GENERAL consiste en la Formulación, Ejecución, Monitoreo y Evaluación de Proyectos Sociales y Productivos, con la finalidad de lograr la integración y el desarrollo  sustentable de comunidades en su contexto, brindando asistencia técnica a proyectos del ámbito público  y privado, e impulsamos a los gobiernos locales hacia la generación de Políticas Públicas acordes a la realidad local.</p>
	<h4 class="titulo_seccion">Nuestra Misión</h4>
	<hr>
	<p>Nuestra misión nos compromete a mejorar el nivel de calidad de vida de las personas y de la comunidades a través de la Integración y el Desarrollo de Comunidades  de manera sustentable, creando en ellas las aptitudes que permitan su desarrollo integral a través de acciones multidisciplinarias regidas por el compromiso y la responsabilidad, basadas en nuestros principios de integración, desarrollo y sustentabilidad pilares para el logro de nuestros objetivos.</p>
	<h4 class="titulo_seccion">Nuestra Visión</h4>
	<hr>
	<p>Nuestra Visión: Constituirnos en una Fundación modelo en brindar apoyo y asistencia técnica a personas físicas y jurídicas, públicas y privadas en la formulación y evaluación de proyectos sociales y productivos que contribuyan a la integración y desarrollo de comunidades, creando a tal fin una red integral que involucre a los distintos sectores sociales, gestionando e impulsando la RSE (Responsabilidad Social Empresaria) en beneficio de las comunidades más vulnerables y con mayores necesidades.</p>
	<h4 class="titulo_seccion">Objetivos Especificos</h4>
	<hr>
	<ul>
		<li>Contribuir al progreso de comunidades</li>
		<li>Acompañar en el desarrollo integral de pobladores en situación de riesgo o exclusión social, potenciando sus capacidades para lograr una participación más activa en la sociedad y en un mercado más dinámico (integración)</li>
		<li>Impulsar alianzas estratégicas dentro de las comunidades y entre comunidades y los gobiernos para facilitar el desarrollo local</li>
		<li>Conectar a la población  a la economía y a la sociedad ayudándoles a adquirir las habilidades a tales efectos</li>
		<li>Fomentar el desarrollo de huertas comunitarias, familiares y escolares a fin de impulsar la autoproducción de alimentos.</li>
		<li>Brindar apoyo y asesoría a los municipios en la gestión de políticas públicas acorde a la realidad local</li>
		<li>Promover proyectos socio-ambientales. (entre otros)</li>
	</ul>
